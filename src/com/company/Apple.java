package com.company;

/**
 * The type Apple.
 */
public class Apple {

    /**
     * The Color.
     */
    private String color;
    /**
     * The Name.
     */
    private String name;
    /**
     * The Size.
     */
    private double size;

    /**
     * Instantiates a new Apple.
     *
     * @param color the color
     * @param name  the name
     * @param size  the size
     */
    public Apple(String color, String name, double size) {
        this.color = color;
        this.name = name;
        this.size = size;
    }


    /**
     * Gets color.
     *
     * @return the color
     */
    public String getColor() {
        return color;
    }

    /**
     * Sets color.
     *
     * @param color the color
     */
    public void setColor(String color) {
        this.color = color;
    }

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets name.
     *
     * @param name the name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * Gets size.
     *
     * @return the size
     */
    public double getSize() {
        return size;
    }

    /**
     * Sets size.
     *
     * @param size the size
     */
    public void setSize(double size) {
        this.size = size;
    }





}
